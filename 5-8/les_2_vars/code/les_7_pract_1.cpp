#include <iostream>
using namespace std;

int main()
{
    int num_1, num_2;

    cout << "Hello!" << endl;
    cout << "Please, enter first integer number" << endl;
    cin >> num_1; 
    cout << "Please, enter second integer number" << endl;
    cin >> num_2;
    cout << "Sum is " << num_1 + num_2 << endl;
    cout << "Dif is " << num_1 - num_2 << endl;
    cout << "Average is " << (num_1 + num_2) / 2 << endl;
    return (0);
}

